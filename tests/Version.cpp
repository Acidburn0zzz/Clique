#include "clique.hpp"
using namespace cliq;

int
main( int argc, char* argv[] )
{
    Initialize( argc, argv );
    mpi::Comm comm = mpi::COMM_WORLD;
    const int commRank = mpi::Rank( comm );

    if( commRank == 0 )
    {
        PrintVersion();
        PrintCCompilerInfo();
        PrintCxxCompilerInfo();
    }

    Finalize();
    return 0;
}
